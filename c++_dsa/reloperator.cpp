#include<iostream>
using namespace std;
int main(){
    int a=5,b=3;
    cout<<"equal to operator "<<(a==b)<<endl;
    cout<<"less than operator a<b "<<(a<b)<<endl;
    cout<<"greater than operator a>b "<<(a>b)<<endl;
    cout<<"greater than equal to operator "<<(a>=5)<<endl;
    cout<<"lesser than equal to operator "<<(b<=a)<<endl;
    cout<<"not equal to an operator "<<(a!=b)<<endl;
    cout<<"all these relational operator returns an boolean value true or false"<<endl;
    return 0;
}